#include	"libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*cpy;

	cpy = s1;
	while (n--)
	{
		if (*s2)
			*s1++ = *s2++;
		else
			*s1++ = '\0';
	}
	return (cpy);
}
