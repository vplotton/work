#include "includes/libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*dest;

	dest = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (dest != NULL)
	{
		dest = ft_strcpy(dest, s1);
		return (dest);
	}
	return (NULL);
}
