int		ft_isalnum(int charter)
{
	if (charter >= 48 && charter <= 57)
		return (1);
	if (charter >= 65 && charter <= 90)
		return (1);
	if (charter >= 97 && charter <= 122)
		return (1);
	return (0);
}
